x = 10
y = 4

# Addition
print("Addition: " ,x + y)

# Subtraction
print("Subtraction: " ,x - y)

# Multiplication
print("Multiplication: " ,x * y)

# Division
print("Division: " ,x / y)

# Floor Division
print("Floor Division: " ,x // y)

# Modulus
print("Modulus: " ,x % y)

# Exponentiation
print("Exponentiation: " ,x ** y)