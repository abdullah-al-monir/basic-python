p = 23
q = 45

# Greater than
print("Greater than: " ,p > q)

# Less than
print("Less than: " ,p < q)

# Equal to
print("Equal to: " ,p == q)

# Not equal to
print("Not equal to: " ,p != q)

# Greater than or equal to
print("Greater than or equal to: " ,p >= q)

# Less than or equal to
print("Less than or equal to: " ,p <= q)

