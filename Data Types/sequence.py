# string

greetings = 'Welcome to my Python World'

print(greetings)

print(type(greetings))

print(greetings[0])  # first index

print(greetings[-1])  # last index

print(greetings[0:5])  # first index to 5th index

print(greetings[0:20:2])  # first index to 20th index with step 2


# List

greetings = ['Welcome', 'to', 'my', 'Python', 'World']

print(greetings)

print(type(greetings))

print(greetings[0])  # first index

print(greetings[-1])  # last index

print(greetings[-3])  # 3rd last index

