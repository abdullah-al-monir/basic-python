# -----------------------------------------------------------------------------
# Description: Demonstrates different ways to create strings in Python.
# -----------------------------------------------------------------------------

# Creating a string with single quotes
single_quote_string = 'Hello, world!'
print(f"Single quotes: {single_quote_string}")

# Creating a string with double quotes
double_quote_string = "Python is powerful."
print(f"Double quotes: {double_quote_string}")

# Creating a multi-line string using triple quotes
multi_line_string = """This is a
multi-line string that
preserves newlines."""
print(f"Multi-line string:\n{multi_line_string}")
